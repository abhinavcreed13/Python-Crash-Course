# --- Rules to naming variables ---

# Rule 1: names can only contain letters, numbers and underscores
# $message = "Hello World"

# variable names can never start with a number
# 2message = "Hello World"

# Rule 2: Spaces are not allowed in variable names
# my message = "Hello World"

# Rule 3: avoid python keywords as variable names
# print = "Hello World"

# Rule 4: Variable names should be short and descriptive
# first_name = "charlie"
# f_n = "charlie"

# print(first_name)
# print(f_n)

# Rule 5: Case sensitive
# firstName = "abc1"
# firstname = "abc2"

# print(firstName)
# print(firstname)

